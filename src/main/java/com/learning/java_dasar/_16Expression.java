package com.learning.java_dasar;

import java.util.Date;

public class _16Expression {
    public static void main(String[] args) {
        // expression adalah konstruksi dari variable, operator, dan pemanggilan method yang mengevaluasi menjadi sebuah single value
        // expression adalah core component dari statement

        int value; // ekspression
        value=10; // ekspression

        System.out.println(value = 100); // statement

        // assigment statement
        double aValue = 8933.234;

        // increment statement
        aValue++;

        // method invocation statement
        System.out.println("Hello World");

        // object creation statement
        Date date = new Date();

        // block
        // block adalah kumpulan statement yang terdiri dari not atau lebih statement
        // block di awali dan diakthiri dengan kurung kurawal
    }
}
