package com.learning.java_dasar;

public class _07TipeDataCharacter {
    public static void main(String[] args) {
        // data character (huruf) di Java direpresentasikan oleh tipe char.
        // untuk membuat data char di java, kita bisa menggunakan tanda '(petik satu) di awal dan di akhir karakter

        char a = 'A';
        char n = 'N';
        char d = 'D';
        char r = 'R';

        System.out.print(a);
        System.out.print(n);
        System.out.print(d);
        System.out.print(r);
        System.out.print(a);
    }
}
